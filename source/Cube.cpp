			glGenBuffers(1, &VBO);

			glBindBuffer(GL_ARRAY_BUFFER, VBO);
			glBufferData(GL_ARRAY_BUFFER, sizeof(cube_vertices_texture_normal), &cube_vertices_texture_normal[0], GL_STATIC_DRAW);

